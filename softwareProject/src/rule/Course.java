package rule;

public class Course {

	String LectureCode;
	String Grade;
	
	
	
	public String getLectureCode() {
		return LectureCode;
	}

	public void setLectureCode(String lectureCode) {
		LectureCode = lectureCode;
	}

	

	public String getGrade() {
		return Grade;
	}

	public void setGrade(String grade) {
		Grade = grade;
	}


	
}
